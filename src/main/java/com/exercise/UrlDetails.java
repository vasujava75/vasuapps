package com.exercise;

public class UrlDetails {
	
	private String version;
	private String title;
	private String headings;
	private int internalLinks;
	private int externalLinks;
	private String loginFormAvailable;

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getHeadings() {
		return headings;
	}

	public void setHeadings(String headings) {
		this.headings = headings;
	}

	public int getInternalLinks() {
		return internalLinks;
	}

	public void setInternalLinks(int internalLinks) {
		this.internalLinks = internalLinks;
	}

	public int getExternalLinks() {
		return externalLinks;
	}

	public void setExternalLinks(int i) {
		externalLinks = i;
	}

	public String getLoginFormAvailable() {
		return loginFormAvailable;
	}

	public void setLoginFormAvailable(String loginFormAvailable) {
		this.loginFormAvailable = loginFormAvailable;
	}

	

}
